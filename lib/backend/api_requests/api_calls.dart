class MuxApiGroup {
  static String baseUrl = 'https://stream.mux.com';
  static Map<String, String> headers = {};
  static GetVideoCall getVideoCall = GetVideoCall();
}

class GetVideoCall {
  Future<ApiCallResponse> call({
    String? playbackId = '0',
  }) async {
    return ApiManager.instance.makeApiCall(
      callName: 'Get Video',
      apiUrl: '${MuxApiGroup.baseUrl}/video/v1/playback-ids/$playbackId',
      callType: ApiCallType.GET,
      headers: {},
      params: {},
      returnBody: true,
      encodeBodyUtf8: false,
      decodeUtf8: false,
      cache: false,
      alwaysAllowBody: false,
    );
  }
}

